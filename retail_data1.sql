-- Retention and Churn --

--  How many customers made repeat purchases?
WITH customers_count as (
	Select Customer_id
	FROM retail_data
	GROUP by Customer_id
	having count(*) > 1
)
Select count(customer_id) as total_repeat_customers
,ROUND(count(customer_id)*100.0/(Select count(distinct customer_id) from retail_data),2) as 'percentage of repeat customers'
FROM customers_count;

--  What % of customers made a repeat purchase within 30 days of their first purchase?
WITH purchases as (
	Select distinct customer_id,invoicedate,
	dense_rank() over(partition by customer_id order by invoicedate) as purchase_date
	FROM retail_data
    ),
min_date as(
Select customer_id,min(invoicedate) as first_purchase
FROM retail_data
GROUP by customer_id
having count(distinct Invoice) > 1
)

Select  count(distinct p.customer_id) as customers_within_30_days,
count(distinct p.customer_id)*100.0/(Select count(distinct customer_id) from retail_data) as '% of within30days purchase'
FROM purchases p
JOIN min_date m
on m.customer_id = p.customer_id
WHERE p.purchase_date = 2 
AND p.invoicedate <= date_add(m.first_purchase ,Interval 30 day);

--  Identify customers who haven’t purchased in the last 3 months(latest date in the dataset)
WITH dates as (
	Select max(invoicedate) as last_date
	FROM retail_data
),
customer_date as(
Select customer_id,max(invoicedate) as latest_purchase
FROM retail_data
GROUP BY customer_id
)
Select c.customer_id as 'NO purchase last 3 months'
FROM dates d
JOIN customer_date c
WHERE c.latest_purchase < date_sub(d.last_date,Interval 3 month);

--  What is the retention rate month-over-month?
WITH monthly_customers as(
	Select distinct customer_id,date_format(invoicedate,'%Y-%m-01') as dates
	FROM retail_data
),
retention as (
	Select m1.dates as dates,
    COUNT(m1.customer_id) as total_customers,
    COUNT(m2.customer_id) as retained_customers -- Customer who purchased in two consecutive month 
    FROM monthly_customers m1
    LEFT JOIN monthly_customers m2
    ON m1.customer_id = m2.customer_id
    AND m2.dates = date_add(m1.dates,Interval 1 month)
    GROUP by m1.dates
)
Select dates,total_customers,retained_customers,
ROUND((retained_customers*100.0/total_customers),2) as retention_rate
FROM retention ;

-- Product Analysis--

 -- What are the top 10 best-selling products (by quantity)?
WITH ranked as (
	 Select Description,SUM(quantity) as total_quantity,
	 dense_rank() over(order by SUM(quantity) desc) as dr
	 FROM retail_data
	 GROUP by Description
)

Select description,total_quantity
FROM ranked
WHERE dr >= 1 and dr <= 10;

--  Which products generate the highest revenue?
Select description,SUM(total_sales) as total_rev
FROM retail_data
GROUP by description
ORDER BY total_rev desc
LIMIT 1;

--  Identify products with high sales but low revenue
WITH stats AS (
    SELECT description,
	SUM(quantity) AS total_qty,
	SUM(total_sales) AS total_rev,
	NTILE(4) OVER (ORDER BY SUM(quantity) DESC) AS qty_quartile,
	NTILE(4) OVER (ORDER BY SUM(total_sales)) AS rev_quartile
    FROM retail_data
    GROUP BY description
)

SELECT description,total_qty,total_rev
FROM stats
WHERE qty_quartile = 1   -- TOP 25% QUANTITY
AND 
rev_quartile = 1; -- BOTTOM 25% REVENUE







