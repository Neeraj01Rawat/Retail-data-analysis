-- REVENUE ANALYSIS -- 

-- What is the total revenue generated by the company?
Select round(sum(total_sales)) as 'Total Revenue Generated'
FROM retail_data;

--  Which month generated the highest revenue?
WITH ranking as(
	Select year(invoicedate) as years,month(invoicedate) months,
	round(sum(total_sales),2) as total_revenue,
    rank() over(partition by year(invoicedate) order by round(sum(total_sales),2) desc)as rnk
	FROM retail_data
    GROUP by year(invoicedate),month(invoicedate)
)

Select years,months,total_revenue
FROM ranking
Where rnk = 1;

--  What is the month-over-month growth in revenue?
WITH growth as (
	Select year(invoicedate) as yrs,month(invoicedate) as mnth,
	SUM(total_sales) as total_revenue
	FROM retail_data
	GROUP by year(invoicedate),month(invoicedate)
),
monthly as(
	Select yrs,mnth,total_revenue,
	lag(total_revenue) over(order by yrs,mnth) as previous_rev
	FROM growth
)
Select yrs,mnth,
(total_revenue-previous_rev) as growth_monthly
FROM monthly;

--  Identify months where revenue declined compared to previous month
WITH growth as (
	Select year(invoicedate) as yrs,month(invoicedate) as mnth,
	SUM(total_sales) as total_revenue
	FROM retail_data
	GROUP by year(invoicedate),month(invoicedate)
),
monthly as(
	Select yrs,mnth,total_revenue,
	lag(total_revenue) over(order by yrs,mnth) as previous_rev
	FROM growth
)
Select yrs,mnth,
(total_revenue-previous_rev) as growth_decline_monthly
FROM monthly
where total_revenue-previous_rev < 0;

--  What percentage of total revenue comes from the top 10 customers?
